package iSBot;

public class pet {
    String name;
    String icon;
    String petEffect;
    String obtained;

    public pet() {
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getPetEffect() {
        return petEffect;
    }

    public void setPetEffect(String petEffect) {
        this.petEffect = petEffect;
    }

    public String getObtained() {
        return obtained;
    }

    public void setObtained(String obtained) {
        this.obtained = obtained;
    }
}
